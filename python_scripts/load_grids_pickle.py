    def show_dataset_details(self, dataset_key=None):
        """Show detailed information about specific dataset or all datasets."""
        if dataset_key:
            datasets_to_show = [dataset_key] if dataset_key in self.loaded_data else []
        else:
            datasets_to_show = list(self.loaded_data.keys())
        
        if not datasets_to_show:
            print("No datasets to show")
            return
        
        print(f"\n=== Dataset Details ===")
        for key in datasets_to_show:
            data = self.loaded_data[key]
            
            print(f"\n--- Dataset: {key}")
            print(f"    Adsorbate: {data['adsorbate']}")
            print(f"    Environment: {data['environment']}")
            print(f"    Zeolite type: {data['zeolite_type']}")
            print(f"    Grid shape: {data['grid_shape']}")
            print(f"    Total grids: {data['total_grids']}")
            print(f"    Original snapshots: {data['snapshots_count']}")
            
            # Label statistics
            valid_labels = sum(1 for label in data['labels'] if label is not None)
            print(f"    Valid labels: {valid_labels}/{len(data['labels'])}")
            
            if valid_labels > 0:
                valid_label_values = [label for label in data['labels'] if label is not None]
                print(f"    Label range: [{min(valid_label_values):.3f}, {max(valid_label_values):.3f}]")
            
            # Show unique snapshot indices
            unique_snapshots = sorted(set(data['snapshot_indices']))
            print(f"    Snapshot indices: {unique_snapshots}")

            # Show grids per snapshot
            snapshot_counts = {}
            for snap_idx in data['snapshot_indices']:
                snapshot_counts[snap_idx] = snapshot_counts.get(snap_idx, 0) + 1
            
            print(f"    Grids per snapshot:")
            for snap_idx in sorted(snapshot_counts.keys()):
                print(f"        Snapshot {snap_idx}: {snapshot_counts[snap_idx]} grids")
            
            # Show voxel_id range
            if 'voxel_ids' in data:
                unique_voxel_ids = sorted(set(data['voxel_ids']))
                print(f"    Voxel IDs (rotation indices): {unique_voxel_ids[:5]}...{unique_voxel_ids[-5:]} (range: {min(unique_voxel_ids)}-{max(unique_voxel_ids)})")
    
    def clear_cache(self):
        """Clear loaded data from memory."""
        self.loaded_data.clear()
        self.all_pickle_files.clear()
        self.feature_names = None  # Reset feature names
        if self.verbose:
            print("Data cache cleared")


